// 字节替换函数
void SubBytes_06(unsigned char state[4][4]) {
    for(int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++) {
            state[i][j] = sbox[state[i][j]];
        }
    }
}

// 行移位函数
int ShiftRows_06(uint8_t (*state)[4]) {
	uint32_t block[4] = {0};
	
	/* i: row */
	for (int i = 0; i < 4; ++i) {
		//便于行循环移位，先把一行4字节拼成uint_32结构，移位后再转成独立的4个字节uint8_t
		LOAD32H(block[i], state[i]);
		block[i] = ROF32(block[i], 8*i);
		STORE32H(block[i], state[i]);
	}
	
	return 0;
}

/* Galois Field (256) Multiplication of two Bytes */
// 两字节的伽罗华域乘法运算
uint8_t GMul(uint8_t u, uint8_t v) {
	uint8_t p = 0;
	
	for (int i = 0; i < 8; ++i) {
		if (u & 0x01) {    //
			p ^= v;
		}
		
		int flag = (v & 0x80);
		v <<= 1;
		if (flag) {
			v ^= 0x1B; /* x^8 + x^4 + x^3 + x + 1 */
		}
		
		u >>= 1;
	}
	
	return p;
}
// 列混合
int MixColumns_06(uint8_t (*state)[4]) {
	uint8_t tmp[4][4];
	uint8_t M[4][4] = {{0x02, 0x03, 0x01, 0x01},
		{0x01, 0x02, 0x03, 0x01},
		{0x01, 0x01, 0x02, 0x03},
		{0x03, 0x01, 0x01, 0x02}};
	
	/* copy state[4][4] to tmp[4][4] */
	for (int i = 0; i < 4; ++i) {
		for (int j = 0; j < 4; ++j){
			tmp[i][j] = state[i][j];
		}
	}
	
	for (int i = 0; i < 4; ++i) {
		for (int j = 0; j < 4; ++j) {  //伽罗华域加法和乘法
			state[i][j] = GMul(M[i][0], tmp[0][j]) ^ GMul(M[i][1], tmp[1][j])
			^ GMul(M[i][2], tmp[2][j]) ^ GMul(M[i][3], tmp[3][j]);
		}
	}
	
	return 0;
}


// 轮密钥加函数
void AddRoundKey_06(unsigned char state[4][4], const unsigned char roundKey[4][4]) {
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++) {
            state[i][j] ^= roundKey[i][j];
        }
    }
}

